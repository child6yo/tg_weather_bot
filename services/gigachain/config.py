from dataclasses import dataclass
from environs import Env


@dataclass
class GigaChat:
    credentials: str
    prompt: str
    forecast_prompt: str


@dataclass
class Config:
    chat: GigaChat


def load_config(path: str | None = None) -> Config:
    env = Env()
    env.read_env(path)
    return Config(chat=GigaChat(credentials=env('GC_CREDINTIALS'), prompt=PROMPT_1, forecast_prompt=FPROMPT_1))